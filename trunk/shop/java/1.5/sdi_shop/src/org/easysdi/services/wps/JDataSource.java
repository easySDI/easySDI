package org.easysdi.services.wps;

public class JDataSource {
	private String jdbcUrl;
	private String jdbcUser;
	private String jdbcPwd;
	private String jPrefix;
	private String jdbcDriver;
	private String jplatformname;
	private String jlanguagefile;
	private String jSender;
	private String jSenderTitle;
	
	public String getJdbcUrl() {
		return jdbcUrl;
	}
	public void setJdbcUrl(String jdbcUrl) {
		this.jdbcUrl = jdbcUrl;
	}
	public String getJdbcUser() {
		return jdbcUser;
	}
	public void setJdbcUser(String jdbcUser) {
		this.jdbcUser = jdbcUser;
	}
	public String getJdbcPwd() {
		return jdbcPwd;
	}
	public void setJdbcPwd(String jdbcPwd) {
		this.jdbcPwd = jdbcPwd;
	}
	public String getjPrefix() {
		return jPrefix;
	}
	public void setjPrefix(String jPrefix) {
		this.jPrefix = jPrefix;
	}
	public String getJdbcDriver() {
		return jdbcDriver;
	}
	public void setJdbcDriver(String jdbcDriver) {
		this.jdbcDriver = jdbcDriver;
	}
	public String getJplatformname() {
		return jplatformname;
	}
	public void setJplatformname(String jplatformname) {
		this.jplatformname = jplatformname;
	}
	public String getJlanguagefile() {
		return jlanguagefile;
	}
	public void setJlanguagefile(String jlanguagefile) {
		this.jlanguagefile = jlanguagefile;
	}
	public String getjSender() {
		return jSender;
	}
	public void setjSender(String jSender) {
		this.jSender = jSender;
	}
	public String getjSenderTitle() {
		return jSenderTitle;
	}
	public void setjSenderTitle(String jSenderTitle) {
		this.jSenderTitle = jSenderTitle;
	}

}
